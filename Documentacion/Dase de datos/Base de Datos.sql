CREATE DEFINER=`root`@`localhost` PROCEDURE `spInsertCursoMateriaProfesor`(IN curso INT, IN materia INT, IN profesor INT)
BEGIN	
	
    DECLARE cant int ;    
    DECLARE idRolProfe int;      
    SELECT idrol INTO idRolProfe FROM USUARIO WHERE dni = profesor; 
        
	SELECT count(*) into cant FROM CursoMateriaProfesor 
	WHERE idcurso = curso and idmateria = materia and dniprofesor = profesor;
    
    #Si el dni pertenece a un rol de profesor y no hay registros materia/profesor para dicho curso entonces lo agrego
    IF idRolProfe = 2 and cant = 0 then
		begin
			INSERT INTO cursoMateriaProfesor (idcurso, idmateria, dniprofesor)
				 VALUES  (curso, materia, profesor);
		end;
        begin
			# luego de insertar el registro materia profesor, hay que actualizar las cantidades en Uno
        	UPDATE curso
			SET cantmate = cantmate+1,
				cantprofe = cantprofe+1
				where idcurso = curso; 
        end;
	end if;
END ;;
